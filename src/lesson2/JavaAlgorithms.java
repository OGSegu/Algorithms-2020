    /**
     * Наибольшая общая подстрока.
     * Средняя
     * <p>
     * Дано две строки, например ОБСЕРВАТОРИЯ и КОНСЕРВАТОРЫ.
     * Найти их самую длинную общую подстроку -- в примере это СЕРВАТОР.
     * Если общих подстрок нет, вернуть пустую строку.
     * При сравнении подстрок, регистр символов *имеет* значение.
     * Если имеется несколько самых длинных общих подстрок одной длины,
     * вернуть ту из них, которая встречается раньше в строке first.
     */
    // Эффективность - O(n * m)
    // Ресуры - O(n * m)
    static public String longestCommonSubstring(String first, String second) {
        char[] firstChars = first.toCharArray();
        char[] secondChars = second.toCharArray();
        int maxFirstIndex = 0;
        int maxSecondIndex = 0;
        int[][] array = new int[firstChars.length + 1][secondChars.length + 1];
        int max = 0;
        for (int i = 1; i <= firstChars.length; i++) {
            for (int j = 1; j <= secondChars.length; j++) {
                if (firstChars[i - 1] == secondChars[j - 1]) {
                    array[i][j] = array[i - 1][j - 1] + 1;
                    if (array[i][j] > max) {
                        max = array[i][j];
                        maxFirstIndex = i;
                        maxSecondIndex = j;
                    }
                }
            }
        }
        StringBuilder sb = new StringBuilder();
        while (array[maxFirstIndex][maxSecondIndex] > 0) {
            sb.append(firstChars[maxFirstIndex - 1]);
            maxFirstIndex--;
            maxSecondIndex--;
        }
        return sb.reverse().toString();
    }

    /**
     * Число простых чисел в интервале
     * Простая
     * <p>
     * Рассчитать количество простых чисел в интервале от 1 до limit (включительно).
     * Если limit <= 1, вернуть результат 0.
     * <p>
     * Справка: простым считается число, которое делится нацело только на 1 и на себя.
     * Единица простым числом не считается.
     */
    // Эффективность - O(n)
    // Ресурсы - O(n)
    static public int calcPrimesNumber(int limit) {
        if (limit <= 1) return 0;
        boolean[] isPrime = new boolean[limit + 1];
        Arrays.fill(isPrime, true);
        for (int i = 2; i * i <= limit; i++) {
            if (isPrime[i]) {
                for (int j = 2 * i; j <= limit; j += i) {
                    isPrime[j] = false;
                }
            }
        }
        int counter = 0;
        for (int i = 2; i <= limit; i++) {
            if (isPrime[i]) counter++;
        }
        return counter;
    }
